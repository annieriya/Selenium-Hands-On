
public class Arrayusingforloop {
	public static void main (String[] args)
	{
	int[] marks = new int[6];
	marks[0]= 0;
	marks[1]= 1;
	marks[2]=2;
	marks[3]=3;
	marks[4]=4;
	marks[5]=6;
	//for loop for integers in the array
	for (int i=0; i< marks.length; i++)
	{
		System.out.println(marks[i]);
	}
	//for loop for String in the array
	String[] names= {"Annie", "Riya","Canberra", "Australia"};
	for (int i=0; i< names.length; i++)
	{
		System.out.println(names[i]);
	}
	}
	}

